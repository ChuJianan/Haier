package yunrui.game.been;

import java.util.ArrayList;
import java.util.List;



public class Question {

	private  int id;
	private  String title;
	private  int answer;
	private  List<Oplist> oplists=new ArrayList<Oplist>();
	public  int getId() {
		return id;
	}
	public  void setId(int id) {
		this.id = id;
	}
	public  String getTitle() {
		return title;
	}
	public  void setTitle(String title) {
		this.title = title;
	}
	public  int getAnswer() {
		return answer;
	}
	public  void setAnswer(int answer) {
		this.answer = answer;
	}
	public  List<Oplist> getOplists() {
		return oplists;
	}
	public  void setOplists(List<Oplist> oplists) {
		this.oplists = oplists;
	}
	
}
